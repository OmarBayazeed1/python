#14.Work with integers and floats in calculations

#int
print('10 + 10=',10+10)
print('10 - 10=',10-10)
print('10 * 10=',10*10)
print('10 / 10=',10/10)

#float
print('10.5 + 10=',10.5+10)
print('10.5 - 10=',10.5-10)
print('10.5 * 10=',10.5*10)
print('10.5 / 10=',10.5/10)
#floor division
print('10 // 10=',10//10)
